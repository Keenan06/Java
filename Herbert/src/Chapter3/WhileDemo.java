// Demonstrate the while loop. 

class WhileDemo {   
  public static void main(String args[]) { 
    char ch; 
 
    ch = 'a'; 
    while(ch <= 'z') { 
      System.out.print(ch); 
      ch++; 
    } 
  }   
}
